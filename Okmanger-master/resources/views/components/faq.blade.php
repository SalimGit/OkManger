<h1 class="grandTitre">À Propos De Deliveroo</h1>
<h2 class="petitTitre">Qui Sommes-Nous ?</h2>
Deliveroo s'est donné pour mission de révolutionner la manière dont vous commandez vos plats. Nous nous sommes associés aux meilleurs restaurants, de la petite adresse locale aux franchises cultes, afin de vous livrer vos plats préférés sur le pas de votre porte.

Choisissez parmi des milliers de suggestions ! Grâce à notre équipe de coursiers, votre commande vous sera livrée en 32 minutes en moyenne.

<h2 class="petitTitre">Pourquoi Deliveroo ?</h2>
Deliveroo est une success story à l'anglaise. Après avoir quitté New York pour s'installer à Londres, notre fondateur a dû se rendre à l'évidence : il lui était quasiment impossible de se faire livrer des plats de qualité. Il s’est donc donné pour mission de créer un service permettant de rapprocher les clients et leurs restaurants préférés.

Nous sommes maintenant présents dans plus de 100 villes du Royaume-Uni, plus de 600 développeurs et salariés travaillent dans notre siège à Londres et nous collaborons régulièrement avec plus 8 000 restaurants et 15 000 coursiers.

<h1 class="grandTitre">Commander Sur Deliveroo</h1>
<h2 class="petitTitre">Comment Ça Marche ?</h2>
Vous pouvez commander soit sur le site, soit via l'application mobile Deliveroo, disponible sur iOS et Android. Saisissez simplement votre code postal pour trouver tous les restaurants disponibles dans votre quartier, choisissez vos plats et passez votre commande.

Une fois que le restaurant aura reçu votre commande, celle-ci sera préparée et soigneusement emballée. Une fois votre commande prête, un coursier Deliveroo viendra la chercher et vous l'apporter.

Si vous voulez vous organiser de manière optimale, vous pouvez également passer votre commande jusqu'à 24 heures à l'avance et planifier une livraison à l'heure de votre choix.

<h2 class="petitTitre">Quels Types De Restaurant Sont Présents Sur Deliveroo ?</h2>
Nous sélectionnons avec attention les meilleurs restaurants de votre quartier. De l'authentique restaurant thaï, aux burgers les plus gourmets, en passant par la pizzeria la plus italienne du coin, Deliveroo ne travaille qu'avec les meilleurs restaurants pour ravir les papilles les plus exigeantes.

<h2 class="petitTitre">Quels Sont Les Horaires De Livraison ?</h2>
Nous effectuons des livraisons du matin jusque tard dans la nuit, et nos restaurants proposent des horaires d'ouverture différents. Consultez la page d'accueil ou l'application pour découvrir quels sont les restaurants disponibles dans votre quartier.

<h2 class="petitTitre">Comment Se Passe La Livraison ?</h2>
Une fois que vous avez validé votre commande, celle-ci est transmise directement au restaurant pour préparation. Une fois celle-ci prête et emballée, un coursier Deliveroo viendra la récupérer pour la livrer à l'adresse indiquée.

<h2 class="petitTitre">Pourquoi Ne Peut-On Pas Régler En Espèces ?</h2>
Nous n’acceptons que les paiements par carte car cela nous permet de vous offrir une expérience client optimale. Cela crée également un environnement de travail plus sécurisant pour nos coursiers. Vous pouvez cependant leur laisser un pourboire en espèces.

<h2 class="petitTitre">Dois-Je Laisser Un Pourboire ?</h2>
C'est à votre discrétion. Vous pouvez le faire directement dans l'application lorsque vous passez votre commande, ou laisser un pourboire en espèces au coursier. Nos livreurs conservent 100 % des pourboires que vous leur laissez.

<h2 class="petitTitre">Y A-T-Il Un Montant Minimum De Commande ?</h2>
Le montant de commande minimum dépend du restaurant auprès duquel vous commandez. Si un montant minimum est exigé, vous en serez informé avant de valider votre commande.

<h2 class="petitTitre">Comment Puis-Je Utiliser Mon Code De Réduction ?</h2>
Si vous avez un code promotionnel, vous pouvez l'utiliser dans l'app ou sur le site web.

Si vous utilisez l'application, connectez-vous à votre compte et ajoutez aisissez votre code dans le champ « Saisissez un code promo ». Sur le site, cliquez sur « Ajouter un code promo » au moment de valider la commande.

<h2 class="petitTitre">Vos Prix Sont-Ils Les Mêmes Que Ceux Proposés En Salle Par Le Restaurant ?</h2>
Nous encourageons nos restaurants à toujours appliquer les mêmes prix pour les plats à livrer que pour le menu proposé en salle, bien qu’il existe des exceptions. Le prix de chaque plat est clairement indiqué dans l'application. Pour toute question concernant les tarifs du menu, veuillez contacter le restaurant directement.

<h2 class="petitTitre">Est-Il Possible De Commander À L’avance ?</h2>
Oui ! Dès la veille, vous pouvez programmer vos commandes et choisir une heure de livraison qui vous convient, à partir de midi.

<h2 class="petitTitre">Puis-Je Emporter Ma Commande ?</h2>
Malheureusement nous ne proposons pas de commandes à emporter. Consultez la page d'accueil de l'application pour découvrir quels restaurants proposent des plats à emporter dans votre quartier.

<h2 class="petitTitre">Comment Les Plats Sont-Ils Emballés ?</h2>
L’emballage dépend toujours du type de plat et du restaurant auprès duquel vous avez passé votre commande. Nos restaurants sont particulièrement attentifs au choix d’un emballage qui maintient la température aussi longtemps que possible.

Si vous avez des suggestions concernant l’emballage / la présentation des plats, n’hésitez pas à nous écrire à hello@deliveroo.fr et nous ferons parvenir vos commentaires au restaurant.

<h1 class="grandTitre">Gestion De Ma Commande</h1>
    <h2 class="petitTitre">Que Faire S’il Y A Un Problème Avec Ma Commande ?</h2>
Notre service client est à votre disposition afin de s'assurer que votre expérience Deliveroo soit la plus agréable possible, à partir du moment où vous passez votre commande jusqu’à son arrivée sur le pas de votre porte.

Nous sommes conscients cependant que tout ne se passe pas toujours comme prévu. Si un problème devait se présenter, vous pouvez utiliser l'Aide dans l'application Deliveroo pour échanger avec notre service client et signaler toute difficulté.

Vous pouvez également écrire à hello@deliveroo.fr ou nous appeler au +33 9 77 55 03 30.

<h2 class="petitTitre">Que Faire Si Je Souhaite Compléter Ma Commande ?</h2>
Vous pouvez contacter le service client via l'Aide dans l'application ou au +33 9 77 55 03 30 et nous ferons tout notre possible pour pour compléter votre commande comme bon vous semble avant que celle-ci ne vous soit livrée.

<h2 class="petitTitre">Que Faire Si Ma Commande Est En Retard ?</h2>
Il arrive parfois que des aléas indépendants de notre volonté génèrent des retards de livraison. Nous nous efforcerons toujours de vous appeler dès que nous pensons que la commande peut avoir du retard, et toute l'équipe fera son maximum pour vous livrer aussi rapidement que possible.

<h2 class="petitTitre">Que Se Passe-T-Il Si Je Ne Suis Pas Là Pour Réceptionner Ma Commande ?</h2>
Si vous réalisez que vous ne serez pas présent pour réceptionner votre commande, nous vous demandons simplement de nous le faire savoir en utilisant l'Aide dans l'application Deliveroo ou en appelant le +33 9 77 55 03 30.

Notre coursier tentera toujours de vous joindre s’il rencontre un problème une fois arrivé à l’adresse de livraison. S’il n’arrive pas à vous contacter, notre service client prendra le relais par téléphone ou e-mail. Veuiller gardez un œil sur vos e-mails au moment de la livraison pour toute mise à jour.

Si nous ne parvenons pas à vous joindre et à vous livrer, notre coursier attendra jusqu’à 10 minutes avant de partir pour livrer une autre commande. ans ce cas, nous devrons malheureusement vous débiter du montant de la commande. Afin éviter d’en arriver là, nous vous conseillons de toujours bien revérifier l'exactitude des coordonnées renseignées.

<h2 class="petitTitre">J’ai reçu un appel provenant du numéro +33 9 70 73 42 30, qui est-ce ?</h2>
Le numéro 0970734230 est utilisé par le livreur quand il veut vous informer de son arrivée s’il ne vous trouve pas tout de suite. Pensez à garder votre téléphone à portée de main pour pouvoir répondre s’il vous appelle, et ainsi assurer la bonne livraison de votre commande.

