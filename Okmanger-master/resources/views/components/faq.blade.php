<h1 class="grandTitre">Est-Ce Que Je Peux Payer Avec Ma Carte Titres-Restaurant Sur Deliveroo?</h1>
Quels Types De Carte Titres-Restaurant Acceptez-Vous?
Actuellement, nous acceptons la carte Ticket Restaurant ® (Edenred), et nous mettons tout en oeuvre pour vous permettre d’utiliser d’autres cartes titres-restaurant le plus rapidement possible . Restez à l’écoute!

<h2 class="petitTitre">Comment Puis-Je Ajouter Ma Carte Ticket Restaurant ® (Edenred)?</h2>
Constituez votre commande et sélectionnez “Ticket Restaurant ® (Edenred)” comme mode de paiement. À partir de l’application, vous pouvez aussi vous rendre sur votre profil, dans « moyens de paiement », sélectionnez « ajouter un moyen de paiement » et choisissez “Ticket Restaurant ® (Edenred)”.

<h2 class="petitTitre">Pourquoi Est-Ce Que Ma Carte Ticket Restaurant ® (Edenred) Est Grisée Au Moment Du Paiement?</h2>
Cela signifie que vous ne pouvez pas payer avec votre carte Ticket Restaurant ® (Edenred). Mais ne vous inquiétez pas, vous pouvez toujours choisir un autre moyen de paiement pour valider votre commande. Si vous voulez payer avec votre carte Ticket Restaurant ® (Edenred), assurez-vous que:

la valeur de votre commande est couverte par votre solde disponible et ne dépasse pas le montant de 19€ (plafond quotidien défini par la réglementation)
votre commande ne contient pas de boissons alcoolisées;
la livraison de votre commande est prévue pour un jour travaillé;
vous n’avez pas de surcoût
Pour le moment, vous ne pouvez pas utiliser le solde de votre carte Ticket Restaurant ® (Edenred) pour laisser un pourboire, donc si vous souhaitez remercier votre livreur, pensez à lui donner en espèce au moment de la livraison.

<h2 class="petitTitre">Est-Ce Que Je Peux Diviser Le Paiement Entre Ma Carte Titres-Restaurant Et Une Autre Carte?</h2>
Non. Le montant total de votre commande doit être couvert par le solde de votre carte Titres-Restaurant.

<h2 class="petitTitre">Puis-Je Me Faire Rembourser Un Paiement Effectuer Avec Ma Carte Titres-Restaurant?</h2>
Si vous avez payé avec votre solde de carte Titres-Restaurant et qu’un remboursement est ultérieurement confirmé, vous le percevrez en crédits Deliveroo.

<h2 class="petitTitre">Puis-Je Programmer Une Commande Pour Plus Tard Et Payer Avec Mon Solde De Carte Titres-Restaurant?</h2>
Oui, tant que votre commande sera livrée le jour même. Si vous souhaitez programmer une commande pour le jour suivant, vous devrez utiliser un autre moyen de paiement.

<h2 class="petitTitre">Est-Ce Que Je Peux Payer Avec Des Chèques Titres-Restaurant? (Titres-Restaurant Papier)</h2>
Non. Pour l’instant, nous n’acceptons que la carte Ticket Restaurant ® (Edenred).

<h1 class="grandTitre">Deliveroo Plus</h1>
<h2 class="petitTitre">Comment ça marche ?</h2>
Deliveroo Plus est un abonnement qui permet aux clients de profiter de la livraison gratuite pour toutes leurs commandes. C’est une manière simple et efficace de profiter encore plus de Deliveroo. Les membres peuvent également profiter d’offres spéciales des restaurants qui leur sont exclusivement réservées.

<h2 class="petitTitre">Comment puis-je m’abonner à Deliveroo Plus ?</h2>
Cette option est disponible sur notre site web ou sur l’app, à condition que vous répondiez aux critères d’éligibilité.

<h2 class="petitTitre">Je ne trouve pas l’option pour m’abonner</h2>
Si vous avez déjà fait livrer une commande Deliveroo en France (et que vous avez enregistré un moyen de paiement sur votre compte), vous devriez voir l’option pour vous abonner.

Les abonnements sont limités à un par client. Si vous avez passé des commandes hors de France, vous ne pouvez pas encore bénéficier du service Plus.

Si vous avez précédemment résilié votre abonnement à Deliveroo Plus et que vous souhaitez le réactiver, contactez le service client à l’adresse hello@deliveroo.fr ou au +33 9 77 55 03 30

<h2 class="petitTitre">Le nombre de commandes est-il limité ?</h2>
Une fois que vous avez souscrit à Deliveroo Plus, il n’y a aucune limite au nombre de commandes que vous pouvez passez avec une livraison gratuite ! Ce service est cependant prévu pour un usage individuel et toutes les commandes doivent respecter les Conditions générales de prestation de service de Deliveroo.

<h2 class="petitTitre">Comment puis-je gérer mon abonnement ?</h2>
Pour gérer votre abonnement Deliveroo Plus, ouvrez la page de votre compte sur notre site web ou sur l’application, puis cliquez sur Deliveroo Plus. Vous pourrez consulter les informations relatives à votre compte, notamment la prochaine date de prélèvement.

<h2 class="petitTitre">À quelle date ont lieu les prélèvements ?</h2>
Les dates de chaque prélèvement sont listées sur la page de votre compte sur notre site web ou dans l’application. Consultez nos Conditions générales de Deliveroo Plus pour plus d’informations.

<h2 class="petitTitre">Puis-je partager mon compte avec d’autres personnes ?</h2>
Deliveroo Plus est destiné uniquement à des clients individuels. Partager un abonnement Deliveroo Plus est contraire à nos conditions d’abonnement et signifie que nous pouvons examiner que votre compte pour vérifier qu’il respecte bien nos Conditions générales de prestation de service Deliveroo Plus.

<h2 class="petitTitre">Comment résilier mon abonnement ?</h2>
Vous pouvez résilier votre abonnement depuis la page de votre compte sur notre site web ou dans l’application, ou en contactant le service client Deliveroo à l’adresse hello@deliveroo.fr ou au +33 9 77 55 03 30.

<h2 class="petitTitre">J’ai résilié mon abonnement par erreur, puis-je le réactiver ?</h2>
Si vous avez accidentellement résilié votre abonnement et souhaitez à nouveau bénéficier de notre service, vous devez vous abonner à nouveau depuis la page de votre compte sur le site web ou dans l’application.

<h2 class="petitTitre">Puis-je modifier les dates de prélèvement ?</h2>
Non, il n’est pas possible de modifier la date des prélèvements pour chaque renouvellement de votre abonnement Deliveroo Plus.

<h2 class="petitTitre">Puis-je me faire rembourser en cas de résiliation ?</h2>
Si vous choisissez de ne pas renouveler votre abonnement Deliveroo Plus, vous pouvez demander un remboursement intégral dans les 14 premiers jours de votre première période payée. Si votre résiliation intervient après les 14 jours du délai de réflexion, vous ne pourrez pas prétendre à un remboursement.

Veuillez contacter le service client Deliveroo à l’adresse hello@deliveroo.fr ou au +33 9 77 55 03 30.

<h2 class="petitTitre">Je suis client Deliveroo for Business. Puis-je bénéficier de la livraison gratuite sur les commandes passées avec mes crédits entreprise ?</h2>
Non, la livraison gratuite ne s’applique pas aux commandes que vous passez avec vos crédits entreprise. Si vous disposez de crédits entreprise sur votre compte, vous pouvez toutefois bénéficier de la livraison gratuite pour vos commandes personnelles passées sans ces crédits.
