Chez Deliveroo, nous utilisons des cookies et d'autres technologies similaires, telles que les pixels et les tags. Ceux-ci permettent à notre site Web de fonctionner, d’être analysé et amélioré, et ils nous permettent également de personnaliser votre expérience et de vous montrer des publicités pertinentes. Voici un aperçu de ce que cela signifie pour vous.

<h2 class="petitTitre">Les cookies, qu'est-ce que c'est ?<h2>
Les cookies sont pas mal du tout. Ils personnalisent votre expérience en ligne et nous aident à vous montrer des publicités Deliveroo pertinentes lorsque vous naviguez sur d'autres sites Web, ils nous aident à comprendre comment vous utilisez notre propre site Web et ils vous font gagner beaucoup de temps en vous rappelant vos informations. Mais comment est-ce que tout cela fonctionne ?

Les cookies sont de petits dossiers conservés sur votre ordinateur ou sur votre téléphone. Ils stockent de petites quantités de données afin que, lorsque vous vous rendez sur un site Web, celui-ci puisse se rappeler de vos préférences et s'assurer d’afficher un contenu pertinent.

Tous les cookies ont des dates d'expiration qui déterminent combien de temps ils restent dans votre navigateur :

Les cookies de session sont des cookies temporaires qui expirent automatiquement lorsque vous fermez votre navigateur.
Les cookies permanents restent généralement dans votre navigateur pendant une période définie ou jusqu'à ce que vous les supprimiez manuellement.
