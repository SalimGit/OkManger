<h2 class="petitTitre">La façon dont nous suivons les emails<h2>
Chaque email que nous vous envoyons contient de petits fichiers gif appelés pixels de suivi.

Il s'agit de petits fichiers graphiques contenant des identifiants uniques nous permettant de reconnaître le moment où nos abonnés du service marketing ont ouvert un e-mail ou cliqué sur certains liens. Cela nous permet d'enregistrer pour chaque abonné : l'adresse e-mail, l'adresse IP, l'identifiant de l'appareil, la date et l'heure associées à chaque ouverture et clic sur une campagne.

Nous utilisons ces données pour créer des rapports sur l'intérêt ou l'utilité de nos campagnes marketing pour nos utilisateurs. Nous pouvons ensuite les ajuster pour rendre nos campagnes plus pertinentes. Par exemple, si vous n'ouvrez jamais d'e-mails à propos des sushis, nous commencerons par supposer que vous ne souhaitez pas recevoir d'e-mails sur les sushi !

